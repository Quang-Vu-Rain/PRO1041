    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        try {
            if (checkAddAll() == false) {
                return;
            } else {
                int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm nhân viên ?");
                if (choice != JOptionPane.YES_OPTION) {
                    return;
                }
                _iNhanVien.add(txtid, txtname, txtpass, cbotenquyennv, txtemail, txtghichu);
//            _iNhanVien.fillToTable(tblnhanvien);
                _iAllNhanVien.fillToTableMaQuyen(tblnhanvien);
                JOptionPane.showMessageDialog(this, "Thêm mới nhân viên thành công !", "Thông báo", 0, iconTick);
                tab.setSelectedIndex(1);
                clear();
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Không thể thêm mới!");
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        try {
            if (checkEditAll() == false) {
                return;
            } else {
                int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn cập nhật nhân viên ?");
                if (choice != JOptionPane.YES_OPTION) {
                    return;
                }
                _iNhanVien.edit(txtid, txtname, txtpass, cbotenquyennv, txtemail, txtghichu);
//            _iNhanVien.fillToTable(tblnhanvien);
                _iAllNhanVien.fillToTableMaQuyen(tblnhanvien);
                JOptionPane.showMessageDialog(this, "Cập nhật nhân viên thành công !", "Thông báo", 0, iconTick);
                tab.setSelectedIndex(1);
                clear();
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Không thể sửa!");
        }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        try {
            int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa nhân viên ?");
            if (choice != JOptionPane.YES_OPTION) {
                return;
            }
            _iNhanVien.delete(txtid);
//        _iNhanVien.fillToTable(tblnhanvien);
            _iAllNhanVien.fillToTableMaQuyen(tblnhanvien);
            JOptionPane.showMessageDialog(this, "Xóa nhân viên thành công !", "Thông báo", 0, iconTrash);
            tab.setSelectedIndex(1);
            clear();
        } catch (Exception e) {
            MsgBox.alert(this, "Không thể xóa");
        }
    }//GEN-LAST:event_btnxoaActionPerformed

