    private void btntiendaucaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntiendaucaActionPerformed
        // Set tiền đầu ca
        int dauca;
        try {
            dauca = Integer.parseInt(JOptionPane.showInputDialog(this, "Nhập vào tiền phát sinh", "Xác nhận", 0));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tiền phải là số !", "Thông báo", 0, iconError);
            return;
        }
        if (dauca < 0) {
            JOptionPane.showMessageDialog(this, "Tiền không hợp lệ !", "Thông báo", 0, iconError);
            return;
        }
        _iGiaoCa.setTiendauca(dauca);
    }//GEN-LAST:event_btntiendaucaActionPerformed

