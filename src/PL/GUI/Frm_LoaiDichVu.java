    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        try {
            if (checkAddAll() == false) {
                return;
            } else {
                int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm loại dịch vụ ?");
                if (choice != JOptionPane.YES_OPTION) {
                    return;
                }
                _iLoaiDV.add(txtmaloaidv, txttendv, txtghichu);
                _iLoaiDV.fillToTable(tbnloaidichvu);
                JOptionPane.showMessageDialog(this, "Thêm loại dịch vụ thành công !", "Thông báo", 0, iconTick);
                clear();
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Không thể thêm mới!");
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        try {
            if (checkEditAll() == false) {
                return;
            } else {
                int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn cập nhật loại dịch vụ ?");
                if (choice != JOptionPane.YES_OPTION) {
                    return;
                }
                _iLoaiDV.edit(txtmaloaidv, txttendv, txtghichu);
                _iLoaiDV.fillToTable(tbnloaidichvu);
                JOptionPane.showMessageDialog(this, "Cập nhật loại dịch vụ thành công !", "Thông báo", 0, iconTick);
                clear();
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Không thể sửa!");
        }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        try {
            int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa loại dịch vụ ?");
            if (choice != JOptionPane.YES_OPTION) {
                return;
            }
            _iLoaiDV.delete(txtmaloaidv);
            _iLoaiDV.fillToTable(tbnloaidichvu);
            JOptionPane.showMessageDialog(this, "Xóa loại dịch vụ thành công !", "Thông báo", 0, iconTrash);
            clear();
        } catch (Exception e) {
            MsgBox.alert(this, "Không thể xóa!");
        }
    }//GEN-LAST:event_btnxoaActionPerformed

