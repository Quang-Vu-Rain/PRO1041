    private void showForm() {
        txtmaloaidv.setText(tbnloaidichvu.getValueAt(_index, 0).toString());
        txttendv.setText(tbnloaidichvu.getValueAt(_index, 1).toString());
        txtghichu.setText(tbnloaidichvu.getValueAt(_index, 2).toString());
    }

    private void clear() {
        txtghichu.setText("");
        txtmaloaidv.setText("");
        txttendv.setText("");
        txtmaloaidv.setEditable(true);
    }

    private boolean checkAddAll() {
        String madv = txtmaloaidv.getText();
        String tendv = txttendv.getText();
        String ghichu = txtghichu.getText();
        if (madv.length() == 0 || tendv.length() == 0 || ghichu.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống dữ liệu !", "Thông báo", 0, iconError);
            return false;
        }
        if (ldv.getListLoaiDichVuByID(madv) != null) {
            JOptionPane.showMessageDialog(this, "Mã loại dịch vụ đã tồn tại !", "Thông báo", 0, iconError);
            return false;
        }
        return true;
    }

    private boolean checkEditAll() {
        String madv = txtmaloaidv.getText();
        String tendv = txttendv.getText();
        String ghichu = txtghichu.getText();
        if (madv.length() == 0 || tendv.length() == 0 || ghichu.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống dữ liệu !", "Thông báo", 0, iconError);
            return false;
        }
        return true;
    }
