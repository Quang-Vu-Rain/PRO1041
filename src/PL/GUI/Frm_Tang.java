    private void showForm() {
        txtmatang.setText(tbltang.getValueAt(_index, 0).toString());
        txtsotang.setText(tbltang.getValueAt(_index, 1).toString());
    }

    private boolean checkAddAll() {
        String maTang = txtmatang.getText();
        int soTang;
        if (maTang.length() == 0 || txtsotang.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống dữ liệu !", "Thông báo", 0, iconError);
            return false;
        }
        try {
            soTang = Integer.parseInt(txtsotang.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Số tầng phải là số nguyên !", "Thông báo", 0, iconError);
            return false;
        }
        if (soTang < 0) {
            JOptionPane.showMessageDialog(this, "Số tầng không phù hợp !", "Thông báo", 0, iconError);
            return false;
        }
        if (tang.getListTangByID(maTang) != null) {
            JOptionPane.showMessageDialog(this, "Mã tầng đã tồn tại !", "Thông báo", 0, iconError);
            return false;
        }
        return true;
    }

    private void clear() {
        txtmatang.setText("");
        txtsotang.setText("");
        txtmatang.setEditable(true);
        txtmatang.setEnabled(true);
    }

    private boolean checkEditAll() {
        String maTang = txtmatang.getText();
        int soTang;
        if (maTang.length() == 0 || txtsotang.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống dữ liệu !", "Thông báo", 0, iconError);
            return false;
        }
        try {
            soTang = Integer.parseInt(txtsotang.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Số tầng phải là số nguyên !", "Thông báo", 0, iconError);
            return false;
        }
        if (soTang < 0) {
            JOptionPane.showMessageDialog(this, "Số tầng không phù hợp !", "Thông báo", 0, iconError);
            return false;
        }
        return true;
    }
