    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        try {
            if (checkAddAll() == false) {
                return;
            } else {
                int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm tầng ?");
                if (choice != JOptionPane.YES_OPTION) {
                    return;
                }
                _iTang.add(txtmatang, txtsotang);
                _iTang.fillToTable(tbltang);
                JOptionPane.showMessageDialog(this, "Thêm tầng thành công !", "Thông báo", 0, iconTick);
                clear();
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Không thể thêm mới!");
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        try {
            if (checkEditAll() == false) {
                return;
            } else {
                int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn cập nhật loại dịch vụ ?");
                if (choice != JOptionPane.YES_OPTION) {
                    return;
                }
                _iTang.edit(txtmatang, txtsotang);
                _iTang.fillToTable(tbltang);
                JOptionPane.showMessageDialog(this, "Cập nhật loại dịch vụ thành công !", "Thông báo", 0, iconTick);
                clear();
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Không thể sửa!");
        }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        try {
            int choice = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa tầng ?");
            if (choice != JOptionPane.YES_OPTION) {
                return;
            }
            _iTang.delete(txtmatang);
            _iTang.fillToTable(tbltang);
            JOptionPane.showMessageDialog(this, "Xóa tầng thành công !", "Thông báo", 0, iconTrash);
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tầng đang được sử dụng !", "Thông báo", 0, iconError);
        }
    }//GEN-LAST:event_btnxoaActionPerformed

